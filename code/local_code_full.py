#@title (local) fast geodesic - Dijkstra graph-distance (final ROIs identical to tvb-gdist)
from scipy.sparse import csr_matrix as _csr
from scipy.sparse.csgraph import dijkstra as _dij
def compute_gdist(verts, faces, source_indices, target_indices=None):
    edges = np.vstack([faces[:, [0,1]], faces[:, [1,2]], faces[:, [0,2]]])
    wlen = np.linalg.norm(verts[edges[:,0]] - verts[edges[:,1]], axis=1)
    n_v = len(verts)
    graph = _csr((np.r_[wlen,wlen], (np.r_[edges[:,0],edges[:,1]], np.r_[edges[:,1],edges[:,0]])), shape=(n_v,n_v))
    dmap = _dij(graph, indices=int(source_indices[0]), directed=False)
    return dmap if target_indices is None else dmap[target_indices]

# @title 2.1.2 Analyze Geodesic Distance (gdist)
def analyze_geodesic_distance_gdist(df_tmaps, iteration_winners, geometry):

    print(f"{boldblue}Running Geodesic Distance Analysis (gdist)...{unbold}")

    distance_rows = []

    # vOTC parcellations of interest
    target_parcels = [
        'G_and_S_occipital_inf', 'G_temporal_inf', 'G_oc-temp_lat-fusifor',
        'S_collat_transv_ant', 'S_collat_transv_post', 'S_temporal_inf',
        'S_occipital_ant', 'S_oc-temp_lat'
    ]

    for subj, winners in iteration_winners.items():
        print(f"  Processing {subj}...")

        # Prepare Geometry for gdist (Must be float64 verts and int32 faces)
        verts = geometry[subj]['pial'].astype(np.float64)
        faces = geometry[subj]['faces'].astype(np.int32)

        annot_names = [n.decode('utf-8') for n in geometry[subj]['annot_names']]
        annot_labels = geometry[subj]['annot_labels']

        # Pre-calculate centroid vertex for each target parcellation
        parcel_centroids = {}
        for target in target_parcels:
            if target in annot_names:
                lbl_idx = annot_names.index(target)
                parcel_verts = np.where(annot_labels == lbl_idx)[0]
                if len(parcel_verts) > 0:
                    # Calculate parcel centroid (vertex closest to mean coordinate)
                    parcel_coords = verts[parcel_verts]
                    mean_coord = np.mean(parcel_coords, axis=0)
                    dists_to_mean = np.linalg.norm(parcel_coords - mean_coord, axis=1)
                    parcel_centroid_vrtx = parcel_verts[np.argmin(dists_to_mean)]
                    parcel_centroids[target] = parcel_centroid_vrtx

        for winner in winners:
            roi_name = winner['combo_name']
            round_num = winner['round']
            roi_verts = winner['combo_union_vertices']

            # 1. Determine ROI Centroid
            roi_coords = verts[roi_verts]
            mean_coord = np.mean(roi_coords, axis=0)
            dists_to_mean = np.linalg.norm(roi_coords - mean_coord, axis=1)
            roi_centroid_vrtx = roi_verts[np.argmin(dists_to_mean)]
            src_indices = np.array([roi_centroid_vrtx], dtype=np.int32)

            # 2. Compute Geodesic Distance from ROI Centroid to ALL vertices
            geo_dists_map = compute_gdist(verts, faces, source_indices=src_indices)

            # 3. Extract distance to each target parcellation's CENTROID
            for pname, parcel_centroid_vrtx in parcel_centroids.items():
                centroid_dist = geo_dists_map[parcel_centroid_vrtx]

                distance_rows.append({
                    'subj': subj,
                    'roi_name': f"R{round_num}_{roi_name}",
                    'target_parcel': pname,
                    'min_geodesic_dist': float(centroid_dist)
                })

    df_dist = pd.DataFrame(distance_rows)
    print(f"{boldgreen}Distance analysis complete. (Rows: {len(df_dist)}){unbold}")
    return df_dist

