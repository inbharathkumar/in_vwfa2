#@title 2.4.2 Stage 2.4 Trimming ROI (session count) [o4]
# Keep vertices appearing in > 50% of the subject's sessions, then enforce contiguity.
# Literature check: functional ROIs are conventionally spatially contiguous (spatial-constraint
# clustering), so after trimming we re-cluster by keeping the LARGEST connected component of the
# survivors (equivalent to re-growing one contiguous ROI; avoids fragmented ROIs).
sessions_per_subj = df_tmaps.groupby('subj')['sess'].nunique().to_dict()
print(f"{boldblue}Stage 2.4: session-count trimming (> 50% of sessions) + contiguity{unbold}")
session_trim_rows = []
trimmed_rois = {}
session_count_maps = {}
for subj in subjects:
    adjacency = build_surface_adjacency(subj)
    n_sessions = sessions_per_subj[subj]; keep_threshold = n_sessions / 2
    count_map = np.zeros(geometry[subj]['inflated'].shape[0], dtype=int)
    trimmed_rois[subj] = []
    for winner in iteration_winners[subj]:
        session_count = {}
        for one_session, cluster in winner['clusters'].items():
            for vrtx_id in cluster:
                session_count[vrtx_id] = session_count.get(vrtx_id, 0) + 1
        union_verts = np.array(winner['combo_union_vertices'])
        counts = np.array([session_count[vrtx_id] for vrtx_id in union_verts])
        count_map[union_verts] = np.maximum(count_map[union_verts], counts)
        kept_verts = union_verts[counts > keep_threshold]
        if len(kept_verts) > 0:
            induced = adjacency[kept_verts][:, kept_verts]
            n_comp, comp_labels = connected_components(induced, directed=False)
            contiguous_verts = kept_verts[comp_labels == np.bincount(comp_labels).argmax()]
        else:
            n_comp, contiguous_verts = 0, kept_verts
        trimmed_rois[subj].append({'roi': winner['combo_name'], 'round': winner['round'],
                                   'orig_verts': union_verts, 'trimmed_verts': contiguous_verts})
        session_trim_rows.append({'subj': subj, 'roi': winner['combo_name'], 'n_sessions': n_sessions,
                                  'n_orig': len(union_verts), 'n_kept': int((counts > keep_threshold).sum()),
                                  'n_trim_contiguous': len(contiguous_verts), 'n_fragments': n_comp,
                                  'pct_retained': round(100*len(contiguous_verts)/len(union_verts), 1)})
    session_count_maps[subj] = count_map
    print(f"  {subj}: {len(iteration_winners[subj])} ROIs trimmed (>{keep_threshold:.1f} of {n_sessions} sessions)")
df_session_trim = pd.DataFrame(session_trim_rows)
df_session_trim.to_csv(join(out_dir, 'cluster_table_sessioncount.csv'), index=False)
to_pickle(trimmed_rois, join(out_dir, 'trimmed_rois.pkl'))
