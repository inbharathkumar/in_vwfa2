#@title 2.5.1 Combined weighting/scoring [o4]
# Combine the three per-ROI factor scores (each normalised 0-1 within subject):
#   parcellation  : stg2_aparc_score      (from 2.1.5 / df_metrics)
#   cortical surf : roi_depth_score        (from 2.2.2)
#   non-target    : roi_nontarget_score    (from 2.3.4)
# Equal weights by default. Vertices are taken from the 2.4 session-trimmed contiguous cores.
weight_aparc, weight_anat, weight_nontarget = 1.0, 1.0, 1.0
print(f"{boldblue}Stage 2.5: combined scoring (aparc + cortical-surface + non-target){unbold}")

df_combined = df_metrics[['subj', 'round', 'roi_key', 'stg2_aparc_score']].copy()
df_combined['roi'] = df_combined['roi_key'].str.split('_', n=1).str[1]
df_combined = df_combined.merge(df_anat_scores[['subj', 'roi', 'roi_depth_score']], on=['subj', 'roi'])
df_combined = df_combined.merge(df_nontarget_scores[['subj', 'roi', 'roi_nontarget_score']], on=['subj', 'roi'])

trimmed_lookup = {(subj, t['roi']): t['trimmed_verts'] for subj in subjects for t in trimmed_rois[subj]}
df_combined['n_trim'] = [len(trimmed_lookup[(s, r)]) for s, r in zip(df_combined.subj, df_combined.roi)]
trimmed_centroid_y = []
for s, r in zip(df_combined.subj, df_combined.roi):
    verts = trimmed_lookup[(s, r)]; pial = geometry[s]['pial']
    if len(verts) > 0:
        roi_xyz = pial[verts]
        trimmed_centroid_y.append(float(roi_xyz[np.argmin(np.linalg.norm(roi_xyz - roi_xyz.mean(0), axis=1)), 1]))
    else:
        trimmed_centroid_y.append(np.nan)
df_combined['centroid_y_trim'] = trimmed_centroid_y

for subj in subjects:
    subj_mask = df_combined['subj'] == subj
    a = norm_0_1(df_combined.loc[subj_mask, 'stg2_aparc_score'])
    d = norm_0_1(df_combined.loc[subj_mask, 'roi_depth_score'])
    n = norm_0_1(df_combined.loc[subj_mask, 'roi_nontarget_score'])
    df_combined.loc[subj_mask, 'stg2_combined_score'] = (weight_aparc*a + weight_anat*d + weight_nontarget*n) / (weight_aparc + weight_anat + weight_nontarget)
df_combined.round(4).to_csv(join(out_dir, 'cluster_table_combined_ALL.csv'), index=False)


